{
	Initialize: function()
	{
		var params = b2RUBELoader.parseWorld(rubeFile, this.m_world);

		this.m_speed = 4;
		this.driveJoint = params.joints['drivejoint'];
		this.chassis = params.bodies['truckchassis'];
	},

	Keyboard: function(key)
	{
		this.parent.prototype.Keyboard.call(this, key);

		switch (key)
		{
		case 'A'.charCodeAt():
			for (var i = 0; i < this.driveJoint.length; ++i)
				this.driveJoint[i].SetMotorSpeed(this.m_speed);
			break;

		case 'S'.charCodeAt():
			for (var i = 0; i < this.driveJoint.length; ++i)
				this.driveJoint[i].SetMotorSpeed(0.0);
			break;

		case 'D'.charCodeAt():
			for (var i = 0; i < this.driveJoint.length; ++i)
				this.driveJoint[i].SetMotorSpeed(-this.m_speed);
			break;
		}
	},

	Step: function()
	{
		this.m_drawStringFunc("Keys: left = a, brake = s, right = d");

		this.m_center.x = this.chassis[0].GetPosition().x;
		this.parent.prototype.Step.call(this);
	}
};

